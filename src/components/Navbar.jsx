const pages = ["Inicio", "Registrarse", "Historico", "Posiciones"];

const MobileMenuTransition = React.forwardRef(function Transition(props, ref) {
  return (
    <motion.div
      ref={ref}
      initial={{ x: "100%" }}
      animate={{ x: 0 }}
      exit={{ x: "100%" }}
      transition={{ type: "spring", stiffness: 120, damping: 10 }}
    >
      <Menu {...props} />
    </motion.div>
  );
});

